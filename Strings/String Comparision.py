#! ================= Comparison Operator =======================
#! -------------- ("<") --------------
s1 = 'abc'
s2 = 'dfg'
print(s2<s1)

#! --------------- (">") ---------------
s1 = 'Mohammed'
s2 = 'jefferson'
print(s1>s2)
#! ----------------- (">=") -----------
s1 = 'amit'
s2 = 'michale'
print(s1 >= s2)
#! ----------------- ("<=") -----------
s1 = 'welcome'
s2 = 'Home'
print(s1<=s2)
#! ----------------- ("==") -----------
s1 = 'batman'
s2 = 'batman'
print(s2 == s1)
#! ----------------- ("!=") -----------
s1 = 'riddler'
s2 = 'scott'
print(s1 != s2)